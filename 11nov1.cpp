#include<iostream>
using namespace std;
int main()
{

int day;
cout<<"Enter day";
cin>>day;

switch(day){
    case 1:
    cout<<"sunday";
    break;

    case 2:
    cout<<"Mon";
    break;

    case 3:
    cout<<"Tue";
    break;

    case 4:
    cout<<"wed";
    break;

    case 5:
    cout<<"Thurs";
    break;

    case 6:
    cout<<"Fri";
    break;

    case 7:
    cout<<"sat";
    break;

    default :
    cout<<"not defined";
}







}








